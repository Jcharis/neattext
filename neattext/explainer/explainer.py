# Get the Value
def __get_value(val, my_dict):
    for key, value in my_dict.items():
        if val == key:
            return value


# Get the Keys
def __get_key(val, my_dict):
    for key, value in my_dict.items():
        if val == value:
            return key


def emoji_explainer(emoji_icon):
    """Returns the Emoji Name or Unicode Name of an Emoji

    Parameter: emoji_icon
    --------
    >>> emoji_explainer('😳')
    'FLUSHED FACE'

    """
    result = __get_key(emoji_icon, __EMOJI_TO_NAME_DICT)
    return result


def unicode_2_emoji(unicode_num):
    """Returns the Emoji Name or Unicode Name of a Unicode

    Parameter: unicode_num
    --------
    >>> unicode_2_emoji('0x1f49b')
    'FLUSHED FACE'

    """
    result = __get_value(unicode_num, __UNICODE_TO_EMOJI_DICT)
    return result


def emojify(emoji_term):
    """Returns the Emoji  of a Term

    Parameter: emoji_term
    --------
    >>> emojify('smiley')
    '😃'

    """
    result = __get_value(emoji_term.upper(), __EMOJI_TO_NAME_DICT)
    return result
